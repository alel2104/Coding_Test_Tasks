
public class Car {
    public String carBrand;
    public int carAge;

    public Car(String carBrand, int carAge) {
        this.carBrand = carBrand;
        this.carAge = carAge;
    }

    public String getCarBrand() {
        return carBrand;
    }

    public void setCarBrand(String carBrand) {
        this.carBrand = carBrand;
    }

    public int getCarAge() {
        return carAge;
    }

    public void setCarAge(int carAge) {
        this.carAge = carAge;
    }
}
